#!/bin/bash

################################################################################
# DNS Parser - Быстрый старт для Linux
# Скопируй в консоль или запусти: bash scripts/QUICKSTART_LINUX.sh
################################################################################

echo "╔════════════════════════════════════════╗"
echo "║  DNS Parser - Быстрый старт (Linux)   ║"
echo "╚════════════════════════════════════════╝"
echo ""

# Проверяем текущую директорию
if [ ! -d "src/dns_shop_parser" ]; then
    echo "❌ Ошибка: скрипт должен быть запущен из корня проекта (где находится src/dns_shop_parser)"
    exit 1
fi

echo "✓ Проект найден в: $(pwd)"
echo ""

# 1. Делаем скрипт исполняемым
echo "📝 Обновление прав доступа..."
chmod +x scripts/dns-parser.sh
echo "✓ dns-parser.sh теперь исполняемый"
echo ""

# 2. Проверяем .env
echo "📋 Проверка конфигурации..."
if [ ! -f ".env" ]; then
    if [ -f ".env.example" ]; then
        echo "⚠ Файл .env не найден, копирую из .env.example..."
        cp .env.example .env
        echo "✓ Создан .env - отредактируй его с корректными значениями:"
        echo "   nano .env"
        echo ""
        echo "Ключевые переменные:"
        echo "  TELEGRAM_TOKEN        - токен Telegram бота"
        echo "  TELEGRAM_CHAT_ID      - ID чата для уведомлений (опционально)"
        echo "  CITY_COOKIE_*         - куки для города"
        echo "  PARSE_INTERVAL        - интервал парсинга в секундах"
        echo "  CHROME_HEADLESS       - false (браузер видимый) или true (без окна)"
    else
        echo "❌ Файл .env.example не найден!"
        exit 1
    fi
else
    echo "✓ Файл .env найден"
fi
echo ""

# 3. Показываем что дальше
echo "════════════════════════════════════════"
echo "✅ Начальная подготовка завершена!"
echo "════════════════════════════════════════"
echo ""
echo "Что дальше:"
echo ""
echo "1️⃣  Отредактируй .env с корректными значениями:"
echo "   nano .env"
echo ""
echo "2️⃣  Запусти интерактивное меню:"
echo "   ./scripts/dns-parser.sh"
echo ""
echo "3️⃣  Выбери пункт 1 для первого запуска"
echo ""
echo "═════════════════════════════════════════════════════════════════"
echo "📖 Полная документация: see docs/LINUX_SETUP.md"
echo "═════════════════════════════════════════════════════════════════"
echo ""
